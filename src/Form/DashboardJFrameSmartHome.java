    private void getData(){
        try {
            String sql = "select * from tbl_smart_home order by id desc limit 1";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            
            rs.next();
            depan = rs.getString("lampu_depan");
            kamar = rs.getString("lampu_kamar");
            belakang = rs.getString("lampu_belakang");
            System.out.println(depan+" | "+kamar+" | "+belakang);
            
            if (depan.equals("1")) {
                jButtonDepan.setBackground(Color.red);
            } else {
                jButtonDepan.setBackground(Color.GRAY);
            }
            if (kamar.equals("1")) {
                jButtonKamar.setBackground(Color.yellow);
            } else {
                jButtonKamar.setBackground(Color.GRAY);
            }
            if (belakang.equals("1")) {
                jButtonBelakang.setBackground(Color.green);
            } else {
                jButtonBelakang.setBackground(Color.GRAY);
            }
            
            rs.close();
            st.close();
        }catch (Exception e){
             Logger.getLogger(DashboardJFrameSmartHome.class.getName()).log(Level.SEVERE, "", e);
        }
    
    }

